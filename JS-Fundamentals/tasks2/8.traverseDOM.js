//Use the firstChild property to access an element.
let firstChild = document.getElementById('title').firstChild;
console.log(firstChild);

//Use the lastChild property to access an element.
let lastChild = document.getElementById('page').lastChild;
console.log(lastChild);

//Use the nextSibling property to access an element.
let nextSibling = document.getElementById('header').nextSibling;
console.log(nextSibling);

//Use the previousSibling property to access an element.
let previousSibling = document.getElementById('title').previousSibling;
console.log(previousSibling);

//Use the parentNode property to access an element.
let parentNode = document.getElementById('content').parentNode;
console.log(parentNode);

//Use the childNodes property to access a group of child elements.
let childNodes = document.getElementById('content').childNodes;
console.log(childNodes);

